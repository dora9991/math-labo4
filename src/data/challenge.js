/** 単元IDから単元を返す */
export function unitById(id) {
  return CHALLENGE_UNITS.find((u) => u.id === id) || null;
}

/** その単元でクリア済みの問題数 */
export function clearedCount(unit, cleared = {}) {
  return unit.problems.filter((p) => cleared[p.id]).length;
}

/** その単元の階級（クリア数で決定。全問クリアで最上位「マスター」） */
export function unitRank(unit, cleared = {}) {
  const done = clearedCount(unit, cleared);
  const total = unit.problems.length;
  const master = CHALLENGE_RANKS[CHALLENGE_RANKS.length - 1];
  if (total > 0 && done >= total) return master;
  let r = CHALLENGE_RANKS[0];
  for (const rank of CHALLENGE_RANKS) {
    if (rank === master) continue; // マスターは全問クリア専用
    if (done >= rank.min) r = rank;
  }
  return r;
}

/** 次の階級まで「あと何問」か（最上位なら null） */
export function toNextRank(unit, cleared = {}) {
  const done = clearedCount(unit, cleared);
  const total = unit.problems.length;
  if (done >= total) return null;
  const cur = unitRank(unit, cleared);
  const curIdx = CHALLENGE_RANKS.indexOf(cur);
  const next = CHALLENGE_RANKS[curIdx + 1];
  if (!next) return null;
  const need = (next === CHALLENGE_RANKS[CHALLENGE_RANKS.length - 1]) ? total : next.min;
  return { rank: next, remain: Math.max(1, need - done) };
}

/** 全単元で「マスター（制覇）」に達した数 */
export function masteredUnits(cleared = {}) {
  return CHALLENGE_UNITS.filter((u) => clearedCount(u, cleared) >= u.problems.length).length;
}

/** 攻略した難問の総数 */
export function totalCleared(cleared = {}) {
  return CHALLENGE_UNITS.reduce((s, u) => s + clearedCount(u, cleared), 0);
}

/** 初クリアで得られるXP（難易度比例・ささやか。くり返しでは入らない＝作業稼ぎ防止） */
export function challengeXp(level) {
  return level * 12;
}
