def next_id(unit_code):
    counters[unit_code] = counters.get(unit_code, 0) + 1
    return f"G4-{unit_code}-{counters[unit_code]:03d}"

def make(unit_code, unit, subunit, fmt, difficulty, q, answer, skill_ids, prereq, error_types, tags_extra=None, note=""):
    exam_level = {1: "基礎", 2: "標準", 3: "応用"}[difficulty]
    tags = [unit, subunit, f"★{difficulty}", exam_level, fmt]
    if tags_extra:
        tags += tags_extra
    return {
        "id": next_id(unit_code),
        "display_grade": "小4",
        "grade": "小4",
        "unit": unit,
        "subunit": subunit,
        "format": fmt,
        "difficulty": difficulty,
        "exam_level": exam_level,
        "skill_ids": skill_ids,
        "prerequisite_skills": prereq,
        "elem_prerequisite_skills": prereq,
        "q": q,
        "answer": answer,
        "source": "オリジナル",
        "note": note,
        "error_types": error_types,
        "tags": tags,
        "autoGradable": True,
        "created_at": TODAY,
    }

