SKILL_MAP = {s["id"]: s for s in SKILL_LIST}

def walk_prereqs(skill_id, visited=None):
    if visited is None:
        visited = set()
    if skill_id not in SKILL_MAP or skill_id in visited:
        return visited
    visited.add(skill_id)
    for p in SKILL_MAP[skill_id].get("prerequisite_skill_ids", []):
        walk_prereqs(p, visited)
    return visited

def prereqs(skill_id):
    all_ids = walk_prereqs(skill_id)
    all_ids.discard(skill_id)
    return sorted(all_ids)

def make(pid, unit, subunit, fmt, diff, exam_level, skill_ids, q, answer, note="", error_types=None, tags_extra=None):
    if error_types is None:
        error_types = []
    all_prereqs = set()
    for sid in skill_ids:
        all_prereqs.update(prereqs(sid))
    for sid in skill_ids:
        all_prereqs.discard(sid)
    elem_pre = sorted(p for p in all_prereqs if p.startswith("sk_E"))
    other_pre = sorted(p for p in all_prereqs if not p.startswith("sk_E"))
    tags = [unit, subunit, f"★{diff}", exam_level, fmt]
    if tags_extra:
        tags += tags_extra
    return {
        "id": pid,
        "display_grade": "中3",
        "grade": "中3",
        "unit": unit,
        "subunit": subunit,
        "format": fmt,
        "difficulty": diff,
        "exam_level": exam_level,
        "skill_ids": skill_ids,
        "prerequisite_skills": other_pre,
        "elem_prerequisite_skills": elem_pre,
        "q": q,
        "answer": answer,
        "source": "オリジナル",
        "note": note,
        "error_types": error_types,
        "tags": tags,
        "autoGradable": True,
        "created_at": TODAY,
    }

