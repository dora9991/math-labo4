# skill_masterを読み込み、前提スキルを再帰的に収集する関数
skill_map = {}

def load_skills():
    global skill_map
    skills = json.loads(SKILL_MASTER_PATH.read_text(encoding="utf-8"))
    for s in skills:
        skill_map[s["id"]] = s

def walk_prereqs(skill_id, visited=None):
    if visited is None:
        visited = set()
    if skill_id in visited:
        return []
    visited.add(skill_id)
    result = []
    s = skill_map.get(skill_id)
    if s:
        for pid in s.get("prerequisite_skill_ids", []):
            result.extend(walk_prereqs(pid, visited))
            if pid not in result:
                result.append(pid)
    return result

def make_problem(pid, unit, subunit, fmt, difficulty, skill_ids, q, answer, error_types=None, note=""):
    exam_level = {1: "基礎", 2: "標準", 3: "応用"}[difficulty]
    all_prereqs = []
    seen = set()
    for sid in skill_ids:
        for p in walk_prereqs(sid):
            if p not in seen:
                seen.add(p)
                all_prereqs.append(p)
    tags = [unit, subunit, f"★{difficulty}", exam_level, fmt]
    return {
        "id": pid,
        "display_grade": "小6",
        "grade": "小6",
        "unit": unit,
        "subunit": subunit,
        "format": fmt,
        "difficulty": difficulty,
        "exam_level": exam_level,
        "skill_ids": skill_ids,
        "prerequisite_skills": all_prereqs,
        "elem_prerequisite_skills": all_prereqs,
        "q": q,
        "answer": str(answer),
        "source": "オリジナル",
        "note": note,
        "error_types": error_types or [],
        "tags": tags,
        "autoGradable": True,
        "created_at": TODAY,
    }

