def next_id(prefix):
    counters[prefix] = counters.get(prefix, 0) + 1
    return f"G1-{prefix}-{counters[prefix]:03d}"

def p(id_, unit, subunit, format_, difficulty, exam_level, skill_ids, q, answer, tags_extra=None, error_types=None, note=""):
    exam_map = {1: "基礎", 2: "標準", 3: "応用"}
    base_tags = [unit, subunit, f"★{difficulty}", exam_level, format_]
    if tags_extra:
        base_tags += tags_extra
    return {
        "id": id_,
        "display_grade": "小1",
        "grade": "小1",
        "unit": unit,
        "subunit": subunit,
        "format": format_,
        "difficulty": difficulty,
        "exam_level": exam_level,
        "skill_ids": skill_ids,
        "prerequisite_skills": [],
        "elem_prerequisite_skills": [],
        "q": q,
        "answer": str(answer),
        "source": "オリジナル",
        "note": note,
        "error_types": error_types or [],
        "tags": list(dict.fromkeys(base_tags)),
        "autoGradable": True,
        "created_at": TODAY,
    }

