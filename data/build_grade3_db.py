skill_map = {s["id"]: s for s in skill_list}

def get_prereqs(skill_ids):
    visited, result = set(), set()
    def walk(sid):
        if sid in visited: return
        visited.add(sid)
        sk = skill_map.get(sid)
        if not sk: return
        for pr in sk.get("prerequisite_skill_ids", []):
            result.add(pr); walk(pr)
    for s in skill_ids: walk(s)
    return sorted(result)

_counters = {}
def make_id(prefix):
    _counters[prefix] = _counters.get(prefix, 0) + 1
    return f"G3-{prefix}-{_counters[prefix]:03d}"

def prob(prefix, grade, unit, subunit, fmt, diff, exam, skill_ids, q, answer, errors, note=None):
    prereqs = get_prereqs(skill_ids)
    elem_prereqs = [x for x in prereqs if x.startswith("sk_E")]
    return {
        "id": make_id(prefix),
        "display_grade": grade, "grade": grade,
        "unit": unit, "subunit": subunit, "format": fmt,
        "difficulty": diff, "exam_level": exam,
        "skill_ids": skill_ids, "prerequisite_skills": prereqs,
        "elem_prerequisite_skills": elem_prereqs,
        "q": q, "answer": str(answer),
        "source": "オリジナル", "note": note or "",
        "error_types": errors,
        "tags": [unit, subunit, f"★{diff}", exam, fmt],
        "autoGradable": True, "created_at": "2026-06-20",
    }

