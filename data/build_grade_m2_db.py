def walk(skill_id, visited=None):
    if visited is None:
        visited = set()
    if skill_id in visited:
        return []
    visited.add(skill_id)
    result = []
    for p in SKILL_PREREQS.get(skill_id, []):
        result.append(p)
        result.extend(walk(p, visited))
    return list(dict.fromkeys(result))

def elem_prereqs(prereqs):
    return [s for s in prereqs if s.startswith("sk_E")]

