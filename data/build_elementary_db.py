OUTPUT = Path(__file__).parent / "problem_bank_elementary.json"
SKILL_FILE = Path(__file__).parent / "skill_master.json"

with open(SKILL_FILE) as f:
    skill_list = json.load(f)
skill_map = {s["id"]: s for s in skill_list}

def get_prereqs(skill_ids):
    visited, result = set(), set()
    def walk(sid):
        if sid in visited: return
        visited.add(sid)
        sk = skill_map.get(sid)
        if not sk: return
        for p in sk.get("prerequisite_skill_ids", []):
            result.add(p); walk(p)
    for s in skill_ids: walk(s)
    return sorted(result)

def p(id, grade, unit, subunit, fmt, diff, exam, skill_ids, q, answer, errors, tags_extra=None):
    prereqs = get_prereqs(skill_ids)
    elem_prereqs = [x for x in prereqs if x.startswith("sk_E")]
    base_tags = [unit, subunit, f"★{diff}", exam, fmt]
    return {
        "id": id, "display_grade": grade, "grade": grade,
        "unit": unit, "subunit": subunit, "format": fmt,
        "difficulty": diff, "exam_level": exam,
        "skill_ids": skill_ids, "prerequisite_skills": prereqs,
        "elem_prerequisite_skills": elem_prereqs,
        "q": q, "answer": answer, "source": "オリジナル",
        "error_types": errors,
        "tags": base_tags + (tags_extra or []),
        "autoGradable": True, "created_at": "2026-06-20",
    }

