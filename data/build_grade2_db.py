def load_skill_map():
    data = json.loads(SKILL_MASTER.read_text())
    return {s["id"]: s for s in data}

def walk_prerequisites(skill_id, skill_map, visited=None):
    if visited is None:
        visited = set()
    if skill_id not in skill_map or skill_id in visited:
        return visited
    visited.add(skill_id)
    for pid in skill_map[skill_id].get("prerequisite_skill_ids", []):
        walk_prerequisites(pid, skill_map, visited)
    return visited

skill_map = load_skill_map()

def prereqs(skill_id):
    """skill_id 自身を除く全前提スキルIDリスト"""
    all_ids = walk_prerequisites(skill_id, skill_map)
    return sorted(all_ids - {skill_id})

